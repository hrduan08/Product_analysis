export type TranscriptTimelineLine = {
  timestamp: string;
  seconds: number;
  start: number;
  startMs: number;
  text: string;
};

export type YtDlpTranscriptSuccess = {
  ok: true;
  source: 'yt-dlp';
  videoId: string;
  languageCode: string;
  languageName: string;
  lineCount: number;
  timeline: TranscriptTimelineLine[];
  transcript: string;
  isAutoGenerated: boolean;
  title: string;
};

export class YtDlpTranscriptError extends Error {
  code: string;
  status: number;

  constructor(code: string, message?: string, status = 500) {
    super(message || code);
    this.name = 'YtDlpTranscriptError';
    this.code = code;
    this.status = status;
  }
}

